package java_code.danyuan02;

public class exe02 {
  public static void main(String[] atgs){

    double num1;
    float num2;

    num1 = 1.0/3.0;                         // double 有16位小数(16digits)
    num2 = 1.0f/3.0f;                       // float 有7位小数（7digits）            float的小数要在后面加上f或F

    System.out.println();                   //换行

    System.out.println("The num1: "+ num1);
    System.out.println("The num2: "+ num2);

  }
}
