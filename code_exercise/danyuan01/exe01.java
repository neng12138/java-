package java_code.danyuan01;

public class exe01 {
  public static void main(String[] args){

    System.out.println();               //      System.out.println()  可以充当换行


//                      System.out.println() 是自动换行的
    System.out.println("Hello world");
    System.out.println("Hello world");



    System.out.println();               //      System.out.println()  可以充当换行



//                      System.out.print() 是不会换行的
    System.out.print("Hello world" + "   ");
    System.out.print("hello world");



    //                     System.out.println()的快捷方式：  sout + Tab(补全)



  }
}
